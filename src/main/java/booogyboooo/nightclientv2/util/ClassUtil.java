package booogyboooo.nightclientv2.util;

import java.util.List;

/**
 * Util for class conversion
 */
public class ClassUtil {
	/**
	 * Convert an Array to a List
	 * <br>
	 * - arrToList(arr)
	 * <br>
	 * <br>
	 * arr
	 * <br>
	 * - An Array of Strings
	 */
	public static List<String> arrToList(String[] arr) {
		return List.of(arr);
	}
}